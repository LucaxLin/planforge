export const generatorUserPrompt = (
  requirement: string,
  framework?: string,
  features?: string,
  style?: string,
  tech?: string,
  otherContext?: string
) => `请根据以下信息生成完整的项目设计文档：

**原始需求：**
${requirement}

**已确定的技术方案：**

框架选择：${framework || '待确定'}

功能模块：${features || '待确定'}

视觉风格：${style || '待确定'}

技术细节：${tech || '待确定'}

${otherContext ? `**其他补充信息：**\n${otherContext}` : ''}

---

**重要要求：**
1. 所有技术选型必须是确定的，不能出现"可以选择"等模糊表述
2. 如果某些信息在对话中没有明确给出，根据最佳实践给出合理的默认值，并在文档中标注"【根据对话确定】"或"【推荐默认值】"
3. 技术栈要具体到版本号或版本范围
4. 文档结构必须完整，包含上述所有章节
5. 功能模块划分要合理，符合项目实际
6. 开发计划要实际可行`;

